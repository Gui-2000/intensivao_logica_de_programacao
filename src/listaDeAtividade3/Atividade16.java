package listaDeAtividade3;

public class Atividade16 {
    //16. Construa um algoritmo que mostre na tela:
    //*
    //**
    //***
    //****
    //*****
    public static void main(String[] args) {

        String letra = "*";
        for (int i=0; i<5; i++) {
            System.out.println(letra);
            letra = letra + "*";
        }
    }
}
